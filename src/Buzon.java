
public class Buzon {
	
	private String id;
	private int tamanio;
	
	public Buzon (String id, int tamanio) {
		this.id = id;
		this.tamanio = tamanio;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public int getTamanio() {
		return tamanio;
	}

	public void setTamanio(int tamanio) {
		this.tamanio = tamanio;
	}

}
